# variable :- used to store values

# Static vs Dynamic Type
# Static Type:- Types of the variable(data) explicitly defined such as in C,C++ and Java
# eg int my_var = 10
# Dynamic Type:- Tyoe of the variable(data) is determine at runtime such as in python
# eg my_var = 10

my_var = 10
print(type(my_var))  # return type of the object
print(id(my_var))  # return address
print(my_var)  # display value of x

my_var = 10.25
print(my_var)

my_var = 'Hello'
print(my_var)

x, y, z = 10, 20, 30
print(x)
print(y)
print(z)
