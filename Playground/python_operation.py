# For addition,substraction,modulus and multiplication,exponential result will be any of one operands
# an example if one operands is float and one int result would be float

print('addition:', 18+30)
print('Substraction:', 30.50-12)
print('multiplication:', 2.5*3)
print('modulues:', 14 % 4)
print('modulues:', 14.75 % 4)
print('exponential:', 2**3)
print('exponential:', 3.5**2)

# Interger Division(Floor Division)
# eg 15//2 first normal division perform 15/4 = 3.75 then Floor of the result = Floor(3.75) = 3

print('Interger Division >>>>', 15//4)  # result Int 3
print('Interger Division >>>>', 15.5//4)  # result Float 3.0


